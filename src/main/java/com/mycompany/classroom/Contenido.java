package com.mycompany.classroom;
/**
 * Clase que representa un contenido en un curso, es unicamente para la informacion basica que se 
 * pide en la clase docente
 */
class Contenido {
    String titulo;
    String texto;

    public Contenido(String titulo, String texto) {
        this.titulo = titulo;
        this.texto = texto;
    }
}